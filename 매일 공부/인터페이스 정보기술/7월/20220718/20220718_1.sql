						,RWX.PURCHS_DTE
						,RWX.PRDUCT_NO
                      FROM  KL_WRHSDLVR RWX
                  LEFT OUTER JOIN SY_ENTRPS BB ON RWX.ENTRPS_SEQNO = BB.ENTRPS_SEQNO
                  LEFT OUTER JOIN SY_CMMN_CODE AA ON RWX.PRPOS_CODE =AA.CMMN_CODE
                  LEFT OUTER JOIN SY_CMMN_CODE AC ON RWX.CSTDY_STTUS_CODE =AC.CMMN_CODE
                  WHERE 1=1
                  AND RWX.PRDUCT_SEQNO =  '2050' /**P*/
                  AND RWX.WRHSDLVR_SE_CODE = 'KL08A00001'


                  ) T
                  ON (RW.UPPER_WRHSDLVR_SEQNO= T.WRHSDLVR_SEQNO OR RW.WRHSDLVR_SEQNO = T.WRHSDLVR_SEQNO)

                  WHERE 1=1

        	AND RW.PRDUCT_SEQNO =  '2050' /**P*/



        ORDER BY RW.WRHSDLVR_DTE,RW.WRHSDLVR_SEQNO DESC;


