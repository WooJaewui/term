SELECT
  A.RCEPT_SEQNO
  ,A.RCEPT_DTE
  ,A.RCEPT_NO
  ,B.RCEPT_SPLORE_SEQNO
  ,B.SPLORE_RCEPT_NO
  ,B.SPLORE_NM
  ,DECODE(B.EXPSS_FEE_AT,'Y','급행','일반') AS EXPSS_FEE_AT
  ,DECODE(B.EXPSS_FEE_AT,'Y',C.EXPRIEM_EXPSS_FEE,C.EXPRIEM_FEE) AS FEE
  ,C.RCEPT_EXPR_IEM_SEQNO
  ,C.EXPRIEM_NM

FROM IM_RCEPT A
JOIN IM_RCEPT_SPLORE B ON A.RCEPT_SEQNO = B.RCEPT_SEQNO
JOIN IM_RCEPT_EXPR_IEM C ON B.RCEPT_SPLORE_SEQNO = C.RCEPT_SPLORE_SEQNO
JOIN IM_RCEPT_ENTRPS D ON A.RCEPT_SEQNO = D.RCEPT_SEQNO

WHERE 1=1
  AND A.DELETE_AT = 'N'
  AND A.INSPCT_INSTT_CODE = 'I000001'
  AND B.DELETE_AT = 'N'
  AND C.DELETE_AT = 'N'
  AND A.RCEPT_DTE   >=    '2019-06-01' /**P*/
  AND A.RCEPT_DTE   <=    '2022-07-18' /**P*/
  AND D.ENTRPS_SE_CODE =  'IM23000001' /**P*/

ORDER BY A.RCEPT_SEQNO;








