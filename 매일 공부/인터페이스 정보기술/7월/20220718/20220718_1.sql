            ,KP.RGNT_STTUS_CODE
			,T.ENTRPS_NM
			,T.VALID_DTE
			,T.CNTNR_QY
			,T.CSTDY_PLACE_CODE
			,T.CSTDY_PLACE_CODE_NM
			,T.UNSEAL_DTE
			,T.USE_REALM
			,T.PRPOS_CODE_NM
			,T.CSTDY_STTUS_CODE_NM
			,T.PC
			,T.CTLG_NO
			,T.CAS_NO
			,T.STNDRD_DC
			,T.REGIST_NO
			,T.CONT
			,T.PRTDG_DC
			,T.MNFCTUR_INSTT_NM
			,T.MNFCTUR_NATION_NM
			,T.LOT_NO
			,T.CPCTY
			,T.DNSTY
			,T.PURCHSQY
			,T.PURCHS_DTE
			,T.PRDUCT_NO
			,SII.INSPCT_INSTT_NM AS DEPTNM
            ,(SELECT CMMN_CODE_NM FROM SY_CMMN_CODE WHERE CMMN_CODE = RW.WRHSDLVR_SE_CODE) AS WRHSDLVRSENM
         		    FROM KL_WRHSDLVR RW
              LEFT OUTER JOIN SY_INSPCT_INSTT SII ON SII.INSPCT_INSTT_CODE = RW.WRHSDLVR_DEPT_CODE
              LEFT OUTER JOIN KL_PRDUCT KP ON KP.PRDUCT_SEQNO = RW.PRDUCT_SEQNO
              LEFT OUTER JOIN
            	 (SELECT
						BB.ENTRPS_NM
						,RWX.WRHSDLVR_SEQNO
						,NVL(RWX.ETC_VALID_DTE,RWX.VALID_DTE) AS VALID_DTE
						,RWX.CNTNR_QY
